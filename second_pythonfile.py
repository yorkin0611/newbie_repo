"""Enter two numbers. Print operations and results of 
the summation, subtraction, multiplication and division."""

a = int(input("Enter the first number: "))
b = int(input("Enter the second number: "))

print(f"{a} + {b} = {a + b}")
print(f"{a} - {b} = {a - b}")
print(f"{a} * {b} = {a * b}")
print(f"{a} / {b} = {a / b}")
print(f"{a} // {b} = {a // b}")
